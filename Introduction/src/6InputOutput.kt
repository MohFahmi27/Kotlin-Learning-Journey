import java.util.*

fun main() {
//    KOTLIN INPUT STRING
    val stringInput = readLine()
    println("String is $stringInput")

//    KOTLIN INPUT INTEGER
//    USING JAVA LIBRARY SCANNER
    val reader = Scanner(System.`in`)
//    nextInt() METHOD IS CALLED WHICH TAKES INTEGER INPUT
    val inpInteger: Int = reader.nextInt()
    println("Integer is $inpInteger")
//    TO GET ANOTHER DATA TYPE CAN ALSO USE nextLong(), nextFloat, etc


}